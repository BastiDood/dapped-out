    <h1>Dapped Out!</h1>
    <p>
        <em>Dapped Out!</em> is a timing-based casino game for the Solana blockchain. The objective of the game is simple:
        try to press a button as close to a targeted marker as possible. The closest contestant to the target wins the greatest
        share in the contested pot.
    </p>
    <p>
        <em>Dapped Out!</em> accepts any valid token in the Solana blockchain as a currency for each contest. When creating
        a new contest, simply point it to the mint account of the token being transacted. For convenience, it is also possible
        to create (only) one brand new program-derived user-generated token instead. The creator has the privilege of minting
        new supply as well as transferring tokens to other wallets.
    </p>
    <p>
        Users may now join the existing contest by staking some tokens into the shared pot. As mentioned earlier, they
        now try their luck in timing the meter as closely as possible. At any point in the future, the contest host may
        close the contest to finally distribute the winnings in proportion to the accuracy of each contestant.
    </p>
